# 1. Crear carpeta del proyecto:       mkdir Proyecto
# 2. Ingresar a la carpeta:            cd Proyecto
# 3. Instalar virtualenv:              pip install virtualenv
# 4. Crear entorno virtual:            python -m venv venv   # Virtual Environment
# 5. Activar entorno virtual:          venv\Scripts\activate   # Windows
#                                      source venv/bin/activate   # Mac/Linux
# 6. Instalar Flask:                   pip install flask
# 7. Ejecutar la aplicación Flask:     python app.py
# 8. Abrir navegador y acceder a:      http://localhost:5000/ping 
# 9. Ejecutar la aplicación como módulo:   python -m app.main
